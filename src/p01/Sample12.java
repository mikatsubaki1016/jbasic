package p01;

public class Sample12 {

	public static void main(String[] args) {
		String data = "ogawa";

		//switch
		switch (data) {
		case "ogawa":
			System.out.println("ogawa");
			break;
		case "kudo":
			System.out.println("kudo");
			break;
		case "oku":
			System.out.println("oku");
			break;
		default:
			System.out.println("無し");
			break;

		}
		

		
	}
}
